<script>
console.log('Budget Blitz game loaded');

let gameScore = 0;
let gameData = {
    totalBudget: 2000,
    remaining: 2000,
    needs: { allocated: 1000, spent: 0 },
    wants: { allocated: 600, spent: 0 },
    savings: { allocated: 400, spent: 0 }
};
let currentScenarioIndex = 0;
let gameCompleted = false;

const gameScenarios = [
