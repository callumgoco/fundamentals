let currentCard = 1;
const totalCards = 5;

function initFlashCards() {
    console.log('Initializing flash cards...');
    
    for (let i = 1; i <= totalCards; i++) {
        const card = document.getElementById(`card-${i}`);
        if (!card) {
            console.error(`Card ${i} not found!`);
            return;
        }
    }
    
    console.log('All cards found, setting up navigation...');
    updateCardDisplay();
    setupCardNavigation();
    console.log('Flash cards initialized successfully');
}

function setupCardNavigation() {
    setTimeout(() => {
        document.addEventListener('click', (e) => {
            if (e.target.closest('.card-nav-btn.prev-btn')) {
                e.preventDefault();
                e.stopPropagation();
                console.log('Previous card clicked, current card:', currentCard);
                if (currentCard > 1) {
                    showCard(currentCard - 1);
                }
            }
            
            if (e.target.closest('.card-nav-btn.next-btn')) {
                e.preventDefault();
                e.stopPropagation();
                console.log('Next card clicked, current card:', currentCard);
                if (currentCard < totalCards) {
                    showCard(currentCard + 1);
                }
            }
            
            if (e.target.classList.contains('dot')) {
                e.preventDefault();
                const cardNumber = parseInt(e.target.getAttribute('data-card'));
                if (cardNumber && cardNumber >= 1 && cardNumber <= totalCards) {
                    showCard(cardNumber);
                }
            }
        });

        document.addEventListener('keydown', (e) => {
            const step1 = document.getElementById('step-1');
            if (step1 && (step1.style.display === 'block' || step1.style.display === '')) {
                if (e.key === 'ArrowLeft' && currentCard > 1) {
                    e.preventDefault();
                    showCard(currentCard - 1);
                } else if (e.key === 'ArrowRight' && currentCard < totalCards) {
                    e.preventDefault();
                    showCard(currentCard + 1);
                }
            }
        });
    }, 100);
}

function showCard(cardNumber) {
    console.log(`Showing card ${cardNumber}, current card: ${currentCard}`);
    
    if (cardNumber < 1 || cardNumber > totalCards) {
        console.error(`Invalid card number: ${cardNumber}`);
        return;
    }
    
    const currentCardEl = document.getElementById(`card-${currentCard}`);
    if (currentCardEl) {
        currentCardEl.classList.remove('active');
        
        if (cardNumber > currentCard) {
            currentCardEl.classList.add('prev');
        }
        
        setTimeout(() => {
            if (currentCardEl) {
                currentCardEl.classList.remove('prev');
            }
        }, 400);
    } else {
        console.warn(`Current card element not found: card-${currentCard}`);
    }

    currentCard = cardNumber;

    const newCardEl = document.getElementById(`card-${currentCard}`);
    if (newCardEl) {
        newCardEl.classList.add('active');
        console.log(`Successfully activated card ${currentCard}`);
    } else {
        console.error(`New card element not found: card-${currentCard}`);
    }

    updateCardDisplay();
}

function updateCardDisplay() {
    const currentCardEl = document.getElementById('current-card');
    const totalCardsEl = document.getElementById('total-cards');
    
    if (currentCardEl) currentCardEl.textContent = currentCard;
    if (totalCardsEl) totalCardsEl.textContent = totalCards;

    const activeCard = document.getElementById(`card-${currentCard}`);
    if (activeCard) {
        const prevBtn = activeCard.querySelector('.card-nav-btn.prev-btn');
        let nextBtn = activeCard.querySelector('.card-nav-btn.next-btn, .start-quiz-btn');
        
        if (prevBtn) prevBtn.disabled = currentCard === 1;
        
        if (nextBtn) {
            if (currentCard === totalCards) {
                if (!nextBtn.classList.contains('start-quiz-btn')) {
                    nextBtn.outerHTML = '<button class="btn start-quiz-btn" data-step="2" style="background: var(--accent) !important; color: white !important; padding: 12px 24px !important; border: none !important; border-radius: 24px !important; font-weight: 600 !important; cursor: pointer !important;">Start Pre-Quiz</button>';
                }
            } else {
                if (nextBtn.classList.contains('start-quiz-btn')) {
                    nextBtn.outerHTML = '<button class="card-nav-btn next-btn"><span>Next ‚Üí</span></button>';
                }
            }
        }

        const dots = activeCard.querySelectorAll('.dot');
        dots.forEach((dot, index) => {
            dot.classList.toggle('active', index + 1 === currentCard);
        });
    }
}

